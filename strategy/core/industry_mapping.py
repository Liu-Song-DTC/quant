def get_industry_category(industry_name: str) -> str:
    """根据行业名称匹配行业分类

    Args:
        industry_name: 原始行业名称

    Returns:
        匹配的行业分类键，或 'default'
    """
    if not industry_name:
        return 'default'

    for category, keywords in INDUSTRY_KEYWORDS.items():
        if any(kw in str(industry_name) for kw in keywords):
            return category
    return 'default'


def get_all_categories() -> list:
    """获取所有行业分类列表"""
    return list(INDUSTRY_KEYWORDS.keys())
